#include "lem_in.h"

int		ft_tubelen(t_tube *len)
{
	int		i;
	t_paths	*tmp;

	tmp = len->links;
	i = 0;
	while (tmp)
	{
		i++;
		tmp = tmp->next;
	}
	return (i);
}
